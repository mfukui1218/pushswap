#include "ft_printf.h"

size_t	ft_num_len(int n)
{
	size_t	c;
	long	num;

	c = 0;
	num = n;
	if (num == 0)
		return (1);
	if (num < 0)
	{
		num *= -1;
		c++;
	}
	while (num)
	{
		num = num / 10;
		c++;
	}
	return (c);
}
